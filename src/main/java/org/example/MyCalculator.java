package org.example;

public class MyCalculator {
    public static int sum(int a, int b){
        return a+b;
    }
    public static int sub(int a, int b){
        return a-b;
    }
    public int mul(int a, int b){
        return a*b;
    }
    public int div(int a, int b){
        return a/b;
    }
    public static Integer[] ArrayInsert(Integer arr[]){
        return arr;
    }
}
